///// ISR /// TIMER 15
void HAL_TIM_PeriodElapsedCallback( TIM_HandleTypeDef *htim)
{
	if (htim == &htim15)
	{
		wyswietl_kolejna_cyferke();
		 HAL_GPIO_TogglePin(LD2_GPIO_Port,LD2_Pin);
	}
}

